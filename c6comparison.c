//////////////////////////////////comparison operator   ///////////////////////////////
// ==,!=,<,>,<=,>=   //
#include<stdio.h>
#include<conio.h>
int main(){
    printf("\n%d", 5 == 5);//1
    printf("\n%d", 5 != 5);//0
    printf("\n%d", 5 < 5);//0
    printf("\n%d", 5 > 5);//0
    printf("\n%d", 5 <= 5);//1
    printf("\n%d", 5 >= 5);//1
    printf("\n%d", 'a' == 'a');//1
    printf("\n%d", "srini" <= "srinivasan");//1
    return 0;
}