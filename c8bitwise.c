///////////////////////////  bitwise operation //////////////////////
//&,|,~,^,>>,<<
#include<stdio.h>
#include<conio.h>
int main(){
    printf("\n%d", 1 & 3);  // 0001 & 0011 = 0001 = 1 
    printf("\n%d", 1 | 3);  // 0001 & 0011 = 0011 = 3
    printf("\n%d", 1 ^ 3);  // 0001 ^ 0011 = 0010 = 2
    printf("\n%d", ~2);     // 0010=-(n+1)=0010+0001=-0011=-3
    printf("\n%d", 3 >> 1); //0011=0001=1
    printf("\n%d", 3 << 1); //0011=0110=6
    getch();
    return 0;
}